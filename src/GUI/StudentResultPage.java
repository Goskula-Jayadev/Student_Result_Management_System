    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollno = roll_no_jTextField.getText();
        try{
        Connection con = ConnectionProviderCode.getcon();
        Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
        ResultSet rs = st.executeQuery("select * from student inner join btech_result2 where student.rollno='"+rollno+"'and btech_result2.rollno='"+rollno+"'");
        if(!rs.first()){ // row not exists with given roll.no
         JOptionPane.showMessageDialog(null, "Entered RollNo. doesn't exists");
        }
        else{
        name_jTextField.setText(rs.getString(1));
        gender_jTextField.setText(rs.getString(3));
        fathername_jTextField.setText(rs.getString(4));
        course_jTextField.setText(rs.getString(5));
        branch_jTextField.setText(rs.getString(6));
        
        physics_jTextField.setText(rs.getString(8));
        chemistry_jTextField.setText(rs.getString(9));
        maths_jTextField.setText(rs.getString(10));
        clang_jTextField.setText(rs.getString(10));
        java_jTextField.setText(rs.getString(12));
        total_jTextField.setText(rs.getString(13));
        
        roll_no_jTextField.setEditable(false);
        name_jTextField.setEditable(false);
        gender_jTextField.setEditable(false);
        fathername_jTextField.setEditable(false);
        course_jTextField.setEditable(false);
        branch_jTextField.setEditable(false);
        
        physics_jTextField.setEditable(false);
        chemistry_jTextField.setEditable(false);
        maths_jTextField.setEditable(false);
        clang_jTextField.setEditable(false);
        java_jTextField.setEditable(false);
        total_jTextField.setEditable(false);
        //verdict_jTextField.setEditable(false);
        
        int phy = Integer.parseInt(physics_jTextField.getText());
        int math = Integer.parseInt(maths_jTextField.getText());
        int chem = Integer.parseInt(chemistry_jTextField.getText());
        int clang = Integer.parseInt(clang_jTextField.getText());
        int java = Integer.parseInt(java_jTextField.getText());
        
        if(phy>=30 && math>=30 && chem>=30 && clang>=30 && java>=30){
        verdict_jTextField.setText("PASS");
        }
        else{
        verdict_jTextField.setText("FAIL");
        }
        verdict_jTextField.setEditable(false);
        }
        }
       catch(Exception e){JOptionPane.showMessageDialog(null, e.toString());}
    }//GEN-LAST:event_formComponentShown

