    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollno = rollno_textfield.getText();
       try{
       Connection con = ConnectionProviderCode.getcon();
        Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
        //
        ResultSet rs = st.executeQuery("select * from btech_result2 where rollno='"+rollno+"'");
        //jTable1.setModel(DbUtils.resultSetToTableModel(rs)); //rs2xml used to auto populate the table
        if(!rs.first()){ // row not exists with given roll.no
         JOptionPane.showMessageDialog(null, "Entered RollNo. doesn't exists");
        }
        else{
        rollno_textfield.setEditable(false);
        physics_textfield.setText(rs.getString(2));
        maths_textfield.setText(rs.getString(3));
        chemistry_textfield.setText(rs.getString(4));
        c_language_textfield.setText(rs.getString(5));
        java_textfield.setText(rs.getString(6));
        }
        }
       catch(Exception e){JOptionPane.showMessageDialog(null, e.toString());}
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String rollno = rollno_textfield.getText();
        int physics = Integer.parseInt( physics_textfield.getText());
        int  maths = Integer.parseInt(maths_textfield.getText());
        int chem = Integer.parseInt(chemistry_textfield.getText());
        int c_language = Integer.parseInt(c_language_textfield.getText());
        int java = Integer.parseInt(java_textfield.getText());
        int total = physics +maths + chem + c_language + java;
      
        try{
        Connection con = ConnectionProviderCode.getcon();
        Statement st = con.createStatement();
        st.executeUpdate("update btech_result2 set physics="+physics+",maths="+maths+",chemistry="+chem+",c_language="+c_language+",java="+java+",total="+total+" where rollno='"+rollno+"'");
        JOptionPane.showMessageDialog(null, "Successfully Added");
        setVisible(false);
        Update_Student_Marks frame = new Update_Student_Marks();
        frame.setVisible(true);
        frame.setLocationRelativeTo(null);
        }
        catch(Exception e){
        JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

